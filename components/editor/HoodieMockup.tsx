export default function HoodieMockup({ selectedView, selectedColor, printArea, canvasRef, hasContent = false }: HoodieMockupProps) {
    const strokeColor = isDarkColor(selectedColor) ? 'rgba(255,255,255,0.45)' : '#222222';

    const getViewComponent = () => {
        switch (selectedView.id) {
            case 'back': return <BackHoodie color={selectedColor} strokeColor={strokeColor} />;
            case 'left-hand': return <LeftHandHoodie color={selectedColor} strokeColor={strokeColor} />;
            case 'right-hand': return <RightHandHoodie color={selectedColor} strokeColor={strokeColor} />;
            default: return <FrontHoodie color={selectedColor} strokeColor={strokeColor} />;
        }
    };

    return (
        <div className="relative w-full" style={{ maxWidth: 500, aspectRatio: '500/540' }}>
            <div className="absolute inset-0 pointer-events-none flex items-start justify-center pt-8" style={{ zIndex: 0 }}>
                {(selectedView.id === 'left-hand' || selectedView.id === 'right-hand') ? (
                    <div style={{ height: '92%', aspectRatio: '271 / 580' }}>
                        {getViewComponent()}
                    </div>
                ) : (
                    <div className="w-[85%] h-[85%]">
                        {getViewComponent()}
                    </div>
                )}
            </div>

            <div className="absolute inset-0 z-20 outline-none focus:outline-none">
                <canvas ref={canvasRef} className="outline-none" />
            </div>

            {!hasContent && (
                <div 
                    className="print-area-placeholder absolute border border-dashed border-gray-400/40 pointer-events-none z-30 flex items-center justify-center text-[10px] text-black/15 uppercase tracking-widest font-medium"
                    style={{
                        left:   `${((printArea?.left ?? 150) / 500) * 100}%`,
                        top:    `${((printArea?.top ?? 175) / 540) * 100}%`,
                        width:  `${((printArea?.width ?? 200) / 500) * 100}%`,
                        height: `${((printArea?.height ?? 220) / 540) * 100}%`,
                    }}
                >
                    PRINT AREA
                </div>
            )}
        </div>
    );
}
