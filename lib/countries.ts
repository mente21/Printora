/** Look up a Country object by ISO code */
export function getCountryByCode(code: string): Country | undefined {
  return COUNTRIES.find(c => c.code === code);
}

/** Look up a Country object by full name */
export function getCountryByName(name: string): Country | undefined {
  return COUNTRIES.find(c => c.name.toLowerCase() === name.toLowerCase());
}

/**
 * Attempt to detect the visitor's country from their IP using the free
 * ip-api.com service (no API key required, 45 req/min limit).
 * Returns an ISO country code string (e.g. "ET") or null on failure.
 */
export async function detectCountryByIP(): Promise<string | null> {
  try {
    const res = await fetch("https://ip-api.com/json/?fields=status,countryCode", {
      signal: AbortSignal.timeout(4000),
    });
    if (!res.ok) return null;
    const json = await res.json();
    if (json.status === "success" && json.countryCode) return json.countryCode as string;
    return null;
  } catch {
    return null;
  }
}
